let age = 10

if (age < 1){
    console.log(`This ${age}-year-old person is an infant.`)
} else if (age >= 1 && age < 3) {
    console.log(`This ${age}-year-old person is a toddler.`)
} else if (age >= 3 && age < 10) {
    console.log(`This ${age}-year-old person is a child.`)
} else if (age >= 10 && age < 13) {
    console.log(`This ${age}-year-old person is a preteen.`)
} else if (age >= 13 && age < 20) {
    console.log(`This ${age}-year-old person is a teen.`)
} else if (age = 20) {
    console.log(`This ${age}-year-old person is a young adult.`)
}
